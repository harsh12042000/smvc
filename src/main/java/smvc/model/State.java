package smvc.model;

public class State {

	private String id;
	private String name;
	
	public State() {
		super();
	}
	public String getId() {
		return id;
	}
	public void setId(String code) {
		this.id = code;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}
