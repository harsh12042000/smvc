package smvc.model;

public class City {

	private Integer id;
	private String state_id;
	private String city;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getState_id() {
		return state_id;
	}
	public void setState_id(String state) {
		this.state_id = state;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	
	
}
